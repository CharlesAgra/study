"use strict";
/*
 *  O que é typescript?
 *  Linguagem desenvolvida pela microsoft
 *  Baseada em javascript
 *  Typescript é um superset do javascript. Então ele tem todas as funcionalidades do javascript
 *  Typescript eu consigo facilmente testar as novas funcionalidades.
 *  Pq no final ele vai ser compilado para uma versão que o navegador ou projeto entende.
 *  Typescript é um javascript com esteróides
*/
/*
 * Vantagens do typescript
 * Fortemente tipada
 * Generics
 * Interfaces
 * Decorators
 * Namespaces
*/
/*
 * Por quê typescript e como utiliza-lo?
 * Como typescript suas variaveis vão ser mais descritivas
 * Suas funções será bem entendida por todos os programadores
 * Com o apenas o javascript comum... O desenvolvimento pode ser dificil..
*/
/*
 * Instalação typescript
 * Comando: npm install -g typescript
*/
/*
 * Iniciando um projeto typescript
 * Comando: tsc --init
 * Vai aparecer uma mensagem de successo
*/
/*
 * Como eu compilo um arquivo?
 * Comando: tsc file.ts
 * Ele vai compilar e gerar o arquivo .js correspondente
 *
 * Comando: tsc -w
 * Esse comando vai escutar todos os arquivos e se tiverem mudanças ele vai compilar automaticamente
*/
